    public ChatGUI(String login, String chataddr, String Port, String idReserv, String LocalIp) {
        initComponents();
        
        chatAddress = chataddr;
        PortChat = Integer.parseInt(Port);
        Login = login;
        idReservation = idReserv;
        IPLocal = LocalIp;
        
        try
        {
            addrGroupe = InetAddress.getByName(chatAddress);
            soc = new MulticastSocket(PortChat);
            soc.joinGroup(addrGroupe);
            soc.setInterface(InetAddress.getByName(LocalIp));
        } catch (UnknownHostException ex) {
            Logger.getLogger(ChatGUI.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(ChatGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        Receiver recv = new Receiver(soc, this);
        recv.start();
        
        
        ConversTA.append("Server > Bienvenue sur le chat: " + chatAddress + ":" + PortChat + "\n");
        ConversTA.append("Server > Votre username est " + login + ".\n");
        if(idReservation != null)
            ConversTA.append("Server > Votre numéro de réservation est " + idReserv + ".\n");
        else
            ConversTA.append("Server > Bonjour cher(e) employé(e)!\n");
        
        GetHelp();
        
        byte[] msg = ("POST_EVENT#" + Login + "#" + "Connected !\n").getBytes();
        
        
        DatagramPacket dp = new DatagramPacket(msg, msg.length, addrGroupe, PortChat);
        try {
            soc.send(dp);
        } catch (IOException ex) {
            Logger.getLogger(ChatGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

