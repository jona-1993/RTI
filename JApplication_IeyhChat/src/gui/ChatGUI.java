    private void SendButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SendButtonActionPerformed
        // TODO add your handling code here:
        byte[] msg;
        Scanner scan = new Scanner(SaisieTF.getText());
        ByteArrayOutputStream os = new ByteArrayOutputStream();
        try
        {
            if(SaisieTF.getText().contains("/help"))
            {
                GetHelp();
                return;
            }
            else if(SaisieTF.getText().contains("/question "))
            {

                scan.next();
                BCDigest digest = new BCDigest("MD5");
                String sequence = scan.next();
                String message = scan.next();
                System.out.println("MESSAGE: " + message);
                
                os.write(("POST_QUESTION#" + digest.CreateDigest(message).length + "#").getBytes());
                
                
                os.write(digest.CreateDigest(message));
                
                os.write(("#" + sequence + "#" + Login + "#" + message + "\n").getBytes());
                
                msg = os.toByteArray();
                
            }
            else if(SaisieTF.getText().contains("/repondre "))
            {
                scan.next();
                
                msg = ("ANSWER_QUESTION#" + Login + "#" + scan.next() + "#" + scan.next() + "\n").getBytes();
            }
            else
                msg = ("POST_EVENT#" + Login + "#" + SaisieTF.getText() + "\n").getBytes();
        
        
            DatagramPacket dp = new DatagramPacket(msg, msg.length, addrGroupe, PortChat);
            soc.send(dp);
            
            SaisieTF.setText(null);
            
        } catch (IOException ex) {
            Logger.getLogger(ChatGUI.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NoSuchAlgorithmException ex) {
            Logger.getLogger(ChatGUI.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NoSuchProviderException ex) {
            Logger.getLogger(ChatGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_SendButtonActionPerformed

