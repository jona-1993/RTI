    public ClientGUI(Socket s, ObjectInputStream oi, ObjectOutputStream oo, String login) {
        initComponents();
        
        PanelJour1.setVisible(false);
        ois = oi;
        oos = oo;
        Login = login;
        
        FUCAMPResponse rep;
        
        try 
        {
            rep = new FUCAMPResponse(FUCAMPResponse.OK, "-", "GetActivitiesDay");
            oos.writeObject(rep);
            
            rep = (FUCAMPResponse)ois.readObject();
            if(rep.getCode() == FUCAMPResponse.OK)
            {
                ActivitesCB.addItem("<<  Activités 1 jour  >>");
                
                for(int i = 0; i < rep.getChargeUtile().split("#").length; i+=1)
                    ActivitesCB.addItem(rep.getChargeUtile().split("#")[i]);
            }
            
            rep = new FUCAMPResponse(FUCAMPResponse.OK, "+", "GetActivitiesDay");
            oos.writeObject(rep);
            
            rep = (FUCAMPResponse)ois.readObject();
            if(rep.getCode() == FUCAMPResponse.OK)
            {
                ActivitesCB.addItem("<< Activités +1 jour >>");
                for(int i = 0; i < rep.getChargeUtile().split("#").length; i+=1)
                    ActivitesCB.addItem(rep.getChargeUtile().split("#")[i]);
            }
            
            RefreshActivities();
            
        } catch (IOException ex) {
            Logger.getLogger(ClientGUI.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(ClientGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

