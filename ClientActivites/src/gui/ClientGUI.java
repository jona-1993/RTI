    private void ReserverButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ReserverButtonActionPerformed
        // TODO add your handling code here:
        
        FUCAMPResponse rep;
        String sender = "";
        String datedebutBrut;
        datedebutBrut = ActivitesCB.getSelectedItem().toString().split("/ ")[1].split(" jour")[0].trim(); // ->/ [x] jour <-
        
        
        try
        {
            SimpleDateFormat f = new SimpleDateFormat("dd/MM/yyyy");
            Date datefin = f.parse(DateTF.getText());
            Calendar c = Calendar.getInstance();
            c.setTime(datefin);
            c.add(Calendar.DATE, Integer.parseInt(datedebutBrut));
            datedebutBrut = f.format(c.getTime());

            sender += Login + "#" + ActivitesCB.getSelectedItem().toString().split("/")[0].trim() + "#" + DateTF.getText() + "#" + datedebutBrut;
            System.err.println(sender);
            rep = new FUCAMPResponse(FUCAMPResponse.OK, sender, "RESERVER");
            
            oos.writeObject(rep);
            
            rep = (FUCAMPResponse)ois.readObject();
            
            RefreshActivities();
            
            if(rep.getCode() == FUCAMPResponse.OK)
                JOptionPane.showMessageDialog(null, "Réservé avec succès", "Success", JOptionPane.INFORMATION_MESSAGE);
        }
        catch(NumberFormatException | ParseException | ClassNotFoundException | IOException e)
        {
            System.err.println(e.getMessage());
        }
        
    }//GEN-LAST:event_ReserverButtonActionPerformed

    private void CancelReservationButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CancelReservationButtonActionPerformed
        // TODO add your handling code here:
        if(RemoveReservationTF.getText().length() > 0){
            FUCAMPResponse rep;
            String sender = "";

            try
            {

                sender = Login + "#" + RemoveReservationTF.getText();
                System.err.println(sender);
                rep = new FUCAMPResponse(FUCAMPResponse.OK, sender, "CancelReservation");

                oos.writeObject(rep);

                rep = (FUCAMPResponse)ois.readObject();

                RefreshActivities();

                if(rep.getCode() == FUCAMPResponse.OK)
                    JOptionPane.showMessageDialog(null, "Supprimé avec succès", "Success", JOptionPane.INFORMATION_MESSAGE);
                else
                    JOptionPane.showMessageDialog(null, "L'id n'existe pas", "Error", JOptionPane.INFORMATION_MESSAGE);
            }
            catch(NumberFormatException | ClassNotFoundException | IOException e)
            {
                JOptionPane.showMessageDialog(null, e.getMessage(), "Error", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_CancelReservationButtonActionPerformed

    private void RefreshActivities() throws IOException, ClassNotFoundException
    {
        FUCAMPResponse rep = new FUCAMPResponse(FUCAMPResponse.OK, Login, "GetReservationsActivites");
        
        oos.writeObject(rep);
        oos.flush();
        
        rep = (FUCAMPResponse)ois.readObject();
        
        ActivitesTextArea.setText("");
        
        if(rep.getCode() == FUCAMPResponse.OK)
            for(int i = 0; i < rep.getChargeUtile().split("#").length; i+=1){
                ActivitesTextArea.append(rep.getChargeUtile().split("#")[i] + "\n");
            }
    }
