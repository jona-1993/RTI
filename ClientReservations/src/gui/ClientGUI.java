    private void ReserverButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ReserverButtonActionPerformed
        // TODO add your handling code here:
        
        ROMPResponse rep;
        String sender = "";
        
        
        try
        {
            sender += LoginTF.getText() + "#" + ActivitesCB.getSelectedItem().toString().split("/ ")[0].split("°")[1] + "#" + DateDebutTF.getText() + "#" + DateFinTF.getText();
            System.err.println(sender);
            rep = new ROMPResponse(ROMPResponse.OK, sender, "BROOM");
            
            oos.writeObject(rep);
            
            rep = (ROMPResponse)ois.readObject();
            
            RefreshRoomsAvalibles();
            RefreshReservations();
            
            if(rep.getCode() == ROMPResponse.OK)
                JOptionPane.showMessageDialog(null, "Réservé avec succès", "Success", JOptionPane.INFORMATION_MESSAGE);
            else
                JOptionPane.showMessageDialog(null, "Une erreur est survenue..", "Error", JOptionPane.INFORMATION_MESSAGE);
            
        }
        catch(NumberFormatException | ClassNotFoundException | IOException e)
        {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Error", JOptionPane.INFORMATION_MESSAGE);
        }
        
    }//GEN-LAST:event_ReserverButtonActionPerformed

    private void CancelReservationButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CancelReservationButtonActionPerformed
        // TODO add your handling code here:
        if(RemoveReservationTF.getText().length() > 0){
            ROMPResponse rep;
            String sender = "";

            try
            {

                sender = LoginModifyTF.getText() + "#" + RemoveReservationTF.getText();
                
                rep = new ROMPResponse(ROMPResponse.OK, sender, "CROOM");

                oos.writeObject(rep);

                rep = (ROMPResponse)ois.readObject();

                RefreshReservations();

                if(rep.getCode() == ROMPResponse.OK)
                    JOptionPane.showMessageDialog(null, "Supprimé avec succès", "Success", JOptionPane.INFORMATION_MESSAGE);
                else
                    JOptionPane.showMessageDialog(null, "L'id n'existe pas", "Error", JOptionPane.INFORMATION_MESSAGE);
            }
            catch(NumberFormatException | ClassNotFoundException | IOException e)
            {
                JOptionPane.showMessageDialog(null, e.getMessage(), "Error", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_CancelReservationButtonActionPerformed

