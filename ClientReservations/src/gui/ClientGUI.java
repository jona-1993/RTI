    private void RefreshRoomsAvalibles() throws IOException, ClassNotFoundException{
        ROMPResponse rep;
        
        rep = new ROMPResponse(ROMPResponse.OK, "-", "GetFreeRooms");
        oos.writeObject(rep);

        DefaultComboBoxModel model = new DefaultComboBoxModel<String>();

        model.addElement("Sélection");

        rep = (ROMPResponse)ois.readObject();

        if(rep.getCode() == ROMPResponse.OK)
        {
            for(int i = 0; i < rep.getChargeUtile().split("#").length; i+=1)
                model.addElement(rep.getChargeUtile().split("#")[i]);
                //ActivitesCB.addItem(rep.getChargeUtile().split("#")[i]);
        }
        ActivitesCB.setModel(model);
        ActivitesCB.setSelectedIndex(0);
        
            
        
    }

