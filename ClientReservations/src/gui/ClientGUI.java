    private void RefreshReservations() throws IOException, ClassNotFoundException
    {
        ROMPResponse rep = new ROMPResponse(ROMPResponse.OK, null, "LROOMS");
        
        oos.writeObject(rep);
        oos.flush();
        
        rep = (ROMPResponse)ois.readObject();
        
        ActivitesTextArea.setText("");
        
        if(rep.getCode() == ROMPResponse.OK)
            for(int i = 0; i < rep.getChargeUtile().split("#").length; i+=1){
                ActivitesTextArea.append(rep.getChargeUtile().split("#")[i] + "\n");
            }
    }
